package com.orderManagement.entity;

public class Company {
	private String cd;
	private String dsec;
	private String dsec_chi;
	
	public String getCd() {
		return cd;
	}
	public void setCd(String cd) {
		this.cd = cd;
	}
	public String getDsec() {
		return dsec;
	}
	public void setDsec(String dsec) {
		this.dsec = dsec;
	}
	public String getDsec_chi() {
		return dsec_chi;
	}
	public void setDsec_chi(String dsec_chi) {
		this.dsec_chi = dsec_chi;
	}
	
}
